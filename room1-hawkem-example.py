"""Multiply two values together."""

def multiply_two_inputs(value1, value2):
    """
    Multiply two values together and return the result.

    Parameters
    ----------
    value1 : int or float
        First value.
    value2 : int or float
        Second value.

    Returns
    -------
    int or float
        The resulting value from multiplying value1 and value2.

    """
    return value1 * value2


multiply_two_inputs(5, 2)
