def add_two_numbers(first_number, second_number):
    """
    The sum of two numbers.

    Parameters
    ----------
    first_number : int or float
        Number to add
    second_number : int or float
        Number to add

    Returns
    -------
    int or float
    """

    return first_number + second_number


def add_multiple_numbers(*args):
    
    return sum(args)


